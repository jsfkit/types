/**
 * Determines how line endpoints are rendered.
 *
 * | Value  | Presentation
 * |--------|------------------------------------------------
 * | `flat` | Equivalent to CSS/SVG linecap value of `butt`
 * | `rnd`  | Equivalent to CSS/SVG linecap value of `round`
 * | `sq`   | Equivalent to CSS/SVG linecap value of `square`
 */
export type LineCapType = 'flat' | 'rnd' | 'sq';
